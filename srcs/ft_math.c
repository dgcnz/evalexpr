int		ft_add(int x, int y)
{
	return (x + y);
}

int		ft_sub(int x, int y)
{
	return (x - y);
}

int		ft_mul(int x, int y)
{
	return (x * y);
}

int		ft_div(int x, int y)
{
	return (x / y);
}

int		ft_mod(int x, int y)
{
	return (x % y);
}
